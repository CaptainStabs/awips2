package com.raytheon.uf.edex.plugin.pointset.netcdf.description;

import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.JAXB;
import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * A collection of {@link ProductDescription}s whose primary role is to serve as
 * a container for {@link JAXB} serialization of multiple descriptions into a
 * single file.
 * 
 * <pre>
 * 
 * SOFTWARE HISTORY
 * 
 * Date          Ticket#  Engineer  Description
 * ------------- -------- --------- --------------------------
 * Aug 11, 2015  4709     bsteffen  Initial creation
 * 
 * </pre>
 * 
 * @author bsteffen
 */
@XmlRootElement
@XmlAccessorType(XmlAccessType.NONE)
public class PointSetProductDescriptions {

    @XmlElement(name = "description")
    private List<ProductDescription> descriptions;

    public List<ProductDescription> getDescriptions() {
        return descriptions;
    }

    public void setDescriptions(List<ProductDescription> descriptions) {
        this.descriptions = descriptions;
    }

    public void addDescription(ProductDescription description) {
        if (this.descriptions == null) {
            this.descriptions = new ArrayList<>();
        }
        this.descriptions.add(description);
    }

    public void addDescriptions(PointSetProductDescriptions descriptions) {
        if (this.descriptions == null) {
            this.descriptions = new ArrayList<>();
        }
        this.descriptions.addAll(descriptions.getDescriptions());
    }
}
